package me.shib.lib.freshrelease.api;

public final class Group {

    private Long id;
    private String label;
    private String name;
    private String description;

    public Long getId() {
        return id;
    }

    public String getLabel() {
        return label;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }
}
